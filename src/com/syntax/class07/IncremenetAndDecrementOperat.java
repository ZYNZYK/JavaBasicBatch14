package com.syntax.class07;

public class IncremenetAndDecrementOperat {
	public static void main(String[] args) {

	
	int num=90;
	num=num+1; //91
	
	System.out.println(num);//another way to increment a variable value by 1 ->>==
	num++;
	System.out.println(num); //91+1=92
	
	//to decrement a value of a variable by 1 ->> -
	num--;
	System.out.println(num); //91
	
	int i =10;
	i--;
	i--;
	System.out.println(i); // only with variables

}
}
